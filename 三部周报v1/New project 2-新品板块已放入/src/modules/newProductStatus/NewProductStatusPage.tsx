const buildMainFindings = (d: any): string[] => {
  const findings: string[] = [];
  const kpi = d.overall.kpi;
  const situ = d.overall.salesSituation;
  const time = d.overall.timeliness;

  // 1. 出单率
  const curRate = situ.soldRate;
  const prevRate = situ.prevSoldRate || '-';
  const hasCompUnsold = d.unsoldReason?.hasCompetitor?.total || 0;
  if (hasCompUnsold > 0) {
    findings.push(`新品出单率 ${curRate}（有对手口径，上期 ${prevRate}），有 ${hasCompUnsold} 个有对手新品未出单拉低整体表现`);
  } else {
    findings.push(`新品出单率 ${curRate}（有对手口径，上期 ${prevRate}），保持稳定`);
  }

  // 2. 销量概况
  if (kpi.totalSalesQty > 0) {
    findings.push(`总销量 ${kpi.totalSalesQty} 件，销售额 $${kpi.totalRevenue.toLocaleString()}（${kpi.revenueChange}），整体${kpi.revenueChange.startsWith('+') ? '增长' : kpi.revenueChange.startsWith('-') ? '下降' : '持平'}`);
  }

  // 3. 品类表现 - 找出销量最高的品类
  const topCat = [...(d.categoryMetrics || [])].sort((a: any, b: any) => (b.curSalesQty || 0) - (a.curSalesQty || 0))[0];
  if (topCat && topCat.curSalesQty > 0) {
    const totalQty = d.categoryMetrics.reduce((s: number, c: any) => s + (c.curSalesQty || 0), 0);
    const pct = totalQty > 0 ? ((topCat.curSalesQty / totalQty) * 100).toFixed(1) : '0';
    findings.push(`「${topCat.category}」贡献最多销量（${topCat.curSalesQty}件，占${pct}%），${topCat.salesQtyChange && topCat.salesQtyChange !== '-' ? `环比${topCat.salesQtyChange.startsWith('+') ? '增长' : '下降'} ${topCat.salesQtyChange}` : '表现稳定'}`);
  }

  // 4. 分析及时率
  const tRate = time.timelyRate;
  const pRate = time.prevTimelyRate || '-';
  findings.push(`分析及时率 ${tRate}（上期 ${pRate}），${tRate && pRate && parseFloat(tRate) >= parseFloat(pRate) ? '持续改善' : '需加强跟进'}`);

  // 5. 分析人亮点 - 找出销量增长最快的
  const topAnalyst = [...(d.analystMetrics || [])].sort((a: any, b: any) => {
    const aG = a.salesQtyChange && a.salesQtyChange !== '-' ? parseFloat(a.salesQtyChange) : 0;
    const bG = b.salesQtyChange && b.salesQtyChange !== '-' ? parseFloat(b.salesQtyChange) : 0;
    return bG - aG;
  })[0];
  if (topAnalyst && topAnalyst.curSalesQty > 0 && topAnalyst.salesQtyChange && topAnalyst.salesQtyChange !== '-') {
    findings.push(`「${topAnalyst.analyst}」负责新品销量 ${topAnalyst.curSalesQty} 件，环比${topAnalyst.salesQtyChange.startsWith('+') ? '增长' : '下降'} ${topAnalyst.salesQtyChange}，表现${topAnalyst.salesQtyChange.startsWith('+') ? '突出' : '需关注'}`);
  }

  // 6. PLP 广告概况
  const plp = d.plpTotal;
  if (plp && plp.cost > 0) {
    findings.push(`PLP广告花费 $${plp.cost.toFixed(2)}，ROAS ${plp.roas}，广告销售额 $${plp.revenue.toFixed(2)}，ACOS ${plp.acos}`);
  }

  return findings;
};

