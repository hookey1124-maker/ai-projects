const buildDynamicAnomalies = (d: any): { severity: string; message: string; affectedItems: string[]; recommendation: string }[] => {
  const anomalies: { severity: string; message: string; affectedItems: string[]; recommendation: string }[] = [];
  const hasCompUnsold = d.unsoldReason?.hasCompetitor;
  const lowShare = d.lowShareRecords || [];

  // 1. 有对手未出单风险
  if (hasCompUnsold?.total > 0) {
    const unsoldSkus = lowShare.filter((r: any) => r.curSalesQty === 0 && (r.curCompetitorQty || 0) > 0);
    anomalies.push({
      severity: 'high',
      message: `本期有 ${hasCompUnsold.total} 个有对手新品未出单（上期 ${hasCompUnsold.prevTotal || 0} 个），均为竞争无优势导致`,
      affectedItems: unsoldSkus.slice(0, 5).map((r: any) => r.sku),
      recommendation: '建议开启 PLP 广告测试，调整定价策略，重点关注王偲涵和朱培源负责的低占比新品',
    });
  }

  // 2. 低占比新品总量风险
  if (lowShare.length >= 30) {
    anomalies.push({
      severity: 'medium',
      message: `低占比新品达 ${lowShare.length} 个（市占<75%且有对手），占比较高，需系统性关注`,
      affectedItems: [],
      recommendation: '建议按品类和负责人分级管理，优先处理可优化的 SKU',
    });
  }

  // 3. PLP广告效果风险
  const plp = d.plpTotal;
  if (plp && plp.acos && parseFloat(plp.acos) > 15) {
    anomalies.push({
      severity: 'medium',
      message: `PLP广告 ACOS ${plp.acos}，ROAS ${plp.roas}，整体效率偏低`,
      affectedItems: [],
      recommendation: '建议优化广告关键词和出价策略，重点提升转化率',
    });
  }

  // 4. 及时率风险
  const tRate = parseFloat(d.overall?.timeliness?.timelyRate || '0');
  if (tRate < 70) {
    anomalies.push({
      severity: 'medium',
      message: `分析及时率仅 ${d.overall.timeliness.timelyRate}，有 ${d.overall.timeliness.noAnalysis8dCount || 0} 个超 8 日未分析`,
      affectedItems: [],
      recommendation: '加强分析跟进机制，确保新品上架后及时完成首次分析',
    });
  }

  return anomalies;
};

